# Integer and Float data types in Python
"""  Integers refer to whole numbers starting from 0.
Floating-point numbers contain decimal values. """

# Intialization
number_1 = 5
number_2 = 6

# Comparison operations
print(number_1 == number_2)
print(number_1 != number_2)
print(number_1 > number_2)
print(number_1 < number_2)
print(number_1 <= number_2)
print(number_1 >= number_2)

# Float input and rounding
value = float(input("Enter a value: "))
print(value)
print(round(value, 1))
